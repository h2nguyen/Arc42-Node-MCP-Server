export function getWorkflowGuide(): string {
  return `# arc42架构文档工作流指南

## 概述

本指南将帮助您使用arc42模板记录软件架构。

## arc42的12个部分

1. **简介和目标** - 定义您要构建什么以及为什么
2. **架构约束** - 您不能做什么？
3. **上下文和范围** - 什么在内部，什么在外部？
4. **解决方案策略** - 高层方法
5. **构建块视图** - 静态结构
6. **运行时视图** - 动态行为
7. **部署视图** - 基础设施
8. **横切概念** - 整个系统中使用的模式
9. **架构决策** - 重要决策
10. **质量要求** - 质量场景
11. **风险和技术债务** - 什么可能出错？
12. **术语表** - 定义术语

## 资源

- [arc42网站](https://arc42.org/)
`;
}

export function getReadmeContent(projectName?: string): string {
  const name = projectName || '项目';
  return `# ${name} - 架构文档

本目录包含${name}的架构文档，基于arc42模板。

## 结构

- \`sections/\` - 单独的部分markdown文件（12个部分）
- \`images/\` - 图表和图像
- \`config.yaml\` - 配置

## 入门

1. 从第1部分开始：简介和目标
2. 迭代地处理各个部分
3. 使用图表来说明概念
`;
}
